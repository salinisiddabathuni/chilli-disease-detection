# ================================================================
# STEP 4: Balancing dataset — 600 images per class
# This fixes class imbalance which causes biased training
# ================================================================

import random

src_balanced_from = "/content/dataset"
dst_balanced = "/content/dataset_balanced"
MAX_PER_CLASS = 600

if os.path.exists(dst_balanced):
    shutil.rmtree(dst_balanced)

os.makedirs(dst_balanced)
print(f"Creating balanced dataset — {MAX_PER_CLASS} images per class...\n")

for cls in CLASS_NAMES:
    src_cls = os.path.join(src_balanced_from, cls)
    dst_cls = os.path.join(dst_balanced, cls)
    os.makedirs(dst_cls)

    all_imgs = [f for f in os.listdir(src_cls)
                if f.lower().endswith(('.jpg', '.jpeg', '.png'))]

    if len(all_imgs) == 0:
        print(f"  {cls}: NO IMAGES FOUND!")
        continue

    if len(all_imgs) < MAX_PER_CLASS:
        # Duplicate images to reach target
        for img in all_imgs:
            shutil.copy(os.path.join(src_cls, img),
                       os.path.join(dst_cls, img))
        extra = MAX_PER_CLASS - len(all_imgs)
        for i in range(extra):
            pick = random.choice(all_imgs)
            shutil.copy(os.path.join(src_cls, pick),
                       os.path.join(dst_cls, f"dup_{i:04d}_{pick}"))
        print(f"  {cls:<20} {len(all_imgs)} original → duplicated to {MAX_PER_CLASS} ✓")
    else:
        selected = random.sample(all_imgs, MAX_PER_CLASS)
        for img in selected:
            shutil.copy(os.path.join(src_cls, img),
                       os.path.join(dst_cls, img))
        print(f"  {cls:<20} {MAX_PER_CLASS} images selected ✓")

print(f"\nBalanced dataset ready! Total: {len(CLASS_NAMES) * MAX_PER_CLASS} images")

# ================================================================
# STEP 5: Install and import all libraries
# ================================================================

import tensorflow as tf
from tensorflow.keras import layers, Model
from tensorflow.keras.applications import MobileNetV2
from tensorflow.keras.preprocessing.image import ImageDataGenerator
import numpy as np
import matplotlib.pyplot as plt

print(f"TensorFlow version: {tf.__version__}")
print(f"GPU available: {len(tf.config.list_physical_devices('GPU')) > 0}")
print("All libraries imported!")

# ================================================================
# STEP 6: Create data generators
# ================================================================

IMAGE_SIZE   = 96
BATCH_SIZE   = 32
NUM_CLASSES  = 8
DATASET_PATH_BALANCED = "/content/dataset_balanced"

# Training generator with augmentation
train_gen = ImageDataGenerator(
    rescale            = 1./255,
    validation_split   = 0.2,
    rotation_range     = 25,
    horizontal_flip    = True,
    vertical_flip      = True,
    zoom_range         = 0.15,
    brightness_range   = [0.8, 1.2],
    shear_range        = 0.1,
    width_shift_range  = 0.1,
    height_shift_range = 0.1,
).flow_from_directory(
    DATASET_PATH_BALANCED,
    target_size  = (IMAGE_SIZE, IMAGE_SIZE),
    batch_size   = BATCH_SIZE,
    class_mode   = "categorical",
    subset       = "training",
    classes      = CLASS_NAMES,
    shuffle      = True,
)

# Validation generator — no augmentation
val_gen = ImageDataGenerator(
    rescale          = 1./255,
    validation_split = 0.2,
).flow_from_directory(
    DATASET_PATH_BALANCED,
    target_size  = (IMAGE_SIZE, IMAGE_SIZE),
    batch_size   = BATCH_SIZE,
    class_mode   = "categorical",
    subset       = "validation",
    classes      = CLASS_NAMES,
    shuffle      = False,
)

print(f"Training images  : {train_gen.samples}")
print(f"Validation images: {val_gen.samples}")
print(f"Steps per epoch  : {len(train_gen)}")
print(f"Classes          : {train_gen.class_indices}")

# ================================================================
# STEP 7: Build MobileNetV2 model
# alpha=0.35 means 35% width — gives small model that fits ESP32
# ================================================================

# Load pretrained MobileNetV2 — already knows edges, textures, shapes
base = MobileNetV2(
    input_shape = (IMAGE_SIZE, IMAGE_SIZE, 3),
    include_top = False,      # Remove ImageNet classifier
    weights     = "imagenet", # Use pretrained weights
    alpha       = 0.35,       # 35% width — small enough for ESP32
)
base.trainable = False  # Freeze base layers for Phase 1

# Add your disease classification layers on top
x   = base.output
x   = layers.GlobalAveragePooling2D()(x)  # Flatten feature maps
x   = layers.Dense(128, activation="relu")(x)
x   = layers.Dropout(0.4)(x)              # Prevent overfitting
out = layers.Dense(NUM_CLASSES, activation="softmax")(x)  # 8 outputs

model = Model(inputs=base.input, outputs=out)

model.compile(
    optimizer = tf.keras.optimizers.Adam(learning_rate=0.001),
    loss      = "categorical_crossentropy",
    metrics   = ["accuracy"]
)

print(f"\nModel built successfully!")
print(f"Total parameters : {model.count_params():,}")
print(f"Base layers      : {len(base.layers)}")
print(f"Input shape      : {model.input_shape}")
print(f"Output shape     : {model.output_shape}")

# ================================================================
# STEP 8: Phase 1 Training — Top layers only
# Trains your new classification head
# Base model stays frozen
# Expected accuracy: 75-87%
# Takes about 10-15 minutes
# ================================================================

print("\n=== PHASE 1: Training top classification layers ===")
print("Base model frozen. Only new Dense layers are training.")
print("Expected accuracy: 75-87%\n")

h1 = model.fit(
    train_gen,
    validation_data = val_gen,
    epochs          = 25,
    callbacks = [
        tf.keras.callbacks.EarlyStopping(
            monitor              = "val_accuracy",
            patience             = 6,
            restore_best_weights = True,
            verbose              = 1
        ),
        tf.keras.callbacks.ReduceLROnPlateau(
            monitor  = "val_loss",
            factor   = 0.3,
            patience = 3,
            verbose  = 1
        ),
        tf.keras.callbacks.ModelCheckpoint(
            "/content/best_phase1.keras",
            monitor        = "val_accuracy",
            save_best_only = True,
            verbose        = 0
        ),
    ],
    verbose = 1,
)

l1, a1 = model.evaluate(val_gen, verbose=0)
print(f"\nPhase 1 complete!")
print(f"Validation accuracy: {a1*100:.1f}%")
print(f"Validation loss    : {l1:.4f}")

# ================================================================
# STEP 9: Phase 2 Training — Fine tuning
# Unfreeze last 10 layers of base model
# Use very small learning rate to avoid catastrophic forgetting
# Expected accuracy: 85-92%
# Takes about 15-20 minutes
# ================================================================

print("\n=== PHASE 2: Fine-tuning last 10 base layers ===")
print("Learning rate reduced 200x to prevent catastrophic forgetting")
print("Expected accuracy: 85-92%\n")

# Unfreeze only last 10 layers of base
base.trainable = True
for layer in base.layers[:-10]:
    layer.trainable = False

trainable_count = sum(1 for l in model.layers if l.trainable)
print(f"Trainable layers now: {trainable_count}")

# Much smaller learning rate for fine-tuning
model.compile(
    optimizer = tf.keras.optimizers.Adam(learning_rate=0.000005),
    loss      = "categorical_crossentropy",
    metrics   = ["accuracy"]
)

h2 = model.fit(
    train_gen,
    validation_data = val_gen,
    epochs          = 25,
    callbacks = [
        tf.keras.callbacks.EarlyStopping(
            monitor              = "val_accuracy",
            patience             = 6,
            restore_best_weights = True,
            verbose              = 1
        ),
        tf.keras.callbacks.ReduceLROnPlateau(
            monitor  = "val_loss",
            factor   = 0.3,
            patience = 3,
            verbose  = 1
        ),
        tf.keras.callbacks.ModelCheckpoint(
            "/content/best_model.keras",
            monitor        = "val_accuracy",
            save_best_only = True,
            verbose        = 1
        ),
    ],
    verbose = 1,
)

l2, a2 = model.evaluate(val_gen, verbose=0)
print(f"\n=== TRAINING COMPLETE ===")
print(f"Phase 1 accuracy: {a1*100:.1f}%")
print(f"Phase 2 accuracy: {a2*100:.1f}%")

if a2 >= 0.85:
    print("Excellent! Accuracy above 85% — proceed to conversion.")
elif a2 >= 0.75:
    print("Good accuracy. Proceed to conversion.")
else:
    print("Accuracy below 75%. Consider adding more images.")

