# ================================================================
# STEP 11: Convert to TFLite INT8 for ESP32 deployment
# Shrinks model from ~2MB to ~766KB
# INT8 = 8-bit integer (1 byte per weight instead of 4 bytes)
# ================================================================

print("\nConverting to TFLite INT8 format...")
print("This may take 5-10 minutes...")

def representative_dataset():
    """Feed 200 sample images to calibrate INT8 quantization"""
    count = 0
    for imgs, _ in val_gen:
        for img in imgs:
            yield [np.expand_dims(img, axis=0).astype(np.float32)]
            count += 1
            if count >= 200:
                return

converter = tf.lite.TFLiteConverter.from_keras_model(model)

# Enable quantization
converter.optimizations             = [tf.lite.Optimize.DEFAULT]
converter.representative_dataset    = representative_dataset
converter.target_spec.supported_ops = [tf.lite.OpsSet.TFLITE_BUILTINS_INT8]
converter.inference_input_type      = tf.uint8   # Input: 0-255 integers
converter.inference_output_type     = tf.uint8   # Output: 0-255 integers

# Convert
tflite_model = converter.convert()

# Save TFLite file
with open("/content/chilli_disease.tflite", "wb") as f:
    f.write(tflite_model)

size_kb = len(tflite_model) / 1024
print(f"\nTFLite model saved!")
print(f"Model size: {size_kb:.1f} KB")

if size_kb < 1000:
    print("PERFECT — fits in ESP32 4MB flash storage!")
elif size_kb < 4000:
    print("OK — fits in ESP32 with SPIFFS")
else:
    print("Too large for ESP32 — contact for further optimization")

